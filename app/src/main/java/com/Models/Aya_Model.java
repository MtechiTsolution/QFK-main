package com.Models;
public class Aya_Model{
     String arbi,urdu,english;

    public Aya_Model(String arbi, String urdu, String english) {
        this.arbi = arbi;
        this.urdu = urdu;
        this.english = english;
    }

    public String getArbi() {
        return arbi;
    }

    public void setArbi(String arbi) {
        this.arbi = arbi;
    }

    public String getUrdu() {
        return urdu;
    }

    public void setUrdu(String urdu) {
        this.urdu = urdu;
    }

    public String getEnglish() {
        return english;
    }

    public void setEnglish(String english) {
        this.english = english;
    }
}
